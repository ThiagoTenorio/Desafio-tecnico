// Inicializa as variáveis
let INDICE = 12;
let SOMA = 0;
let K = 1;

// Loop que calcula a soma
while (K < INDICE) {
    K = K + 1; // Incrementa K
    SOMA = SOMA + K; // Adiciona o novo valor de K à SOMA
    console.log("O valor da variável SOMA é:", SOMA); // 77 (último resultado)
}

